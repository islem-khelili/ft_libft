#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*arr;

	if (nmemb == 0 || size == 0)
		return (malloc(0));
	if (nmemb > __SIZE_MAX__ / size)
		return (NULL);
	arr = (void *)malloc(nmemb * size);
	if (arr == NULL )
		return (NULL);
	ft_bzero(arr, (nmemb * size));
	return (arr);
}
